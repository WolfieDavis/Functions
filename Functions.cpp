int add(int a, int b)
{
	return a + b;
}

int subtract(int a, int b)
{
	return a - b;
}

int multiply(int a, int b)
{
	return a * b;
}

float divide(float a, float b)
{
	cout << "float version" << endl; 

	return a / b;
}

int divide(int a, int b)
{
	cout << "int version" << endl;

	return a / b;
}
